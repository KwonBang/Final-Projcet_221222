		//사람은 같은 것으로 이해할 수 있지만 컴퓨터는 전혀 다른 것으로 이해한다 위는 문자,아래는 문자열 전혀 다르게 취급한다.
		System.out.println();	//출력후 줄을 바꾸는 명령어
		
		
		
		
		
		
